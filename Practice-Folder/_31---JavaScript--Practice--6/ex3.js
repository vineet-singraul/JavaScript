let str = "javascript";

if (str === str.toUpperCase()) {
  console.log("The string is already in uppercase.");
} else {
  console.log("Converting to uppercase: " + str.toUpperCase());
}

if (str === str.toLowerCase()) {
  console.log("The string is already in lowercase.");
} else {
  console.log("Converting to lowercase: " + str.toLowerCase());
}
